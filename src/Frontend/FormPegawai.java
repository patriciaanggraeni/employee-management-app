    private void tblPegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPegawaiMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblPegawai.getModel();
        int row = tblPegawai.getSelectedRow();
        Pegawai pegawai = new Pegawai();

        pegawai = pegawai.getByid(Integer.parseInt(model.getValueAt(row, 0).toString()));
        inputNipPegawai.setText(pegawai.getNip());
        dataJabatan.getModel().setSelectedItem(pegawai.getJabatan().getIdJabatan());
        dataGolongan.getModel().setSelectedItem(pegawai.getGolongan().getIdGolongan());
        inputNamaPegawai.setText(pegawai.getNama());
        inputNoTelphPegawai.setText(pegawai.getNoTelp());
        inputEmailPegawai.setText(pegawai.getEmail());
        inputTglLahirPegawai.setText(pegawai.getTanggalLahir());
        inputAlamatPegawai.setText(pegawai.getAlamat());
    }//GEN-LAST:event_tblPegawaiMouseClicked

    private void btnSimpanPegawaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanPegawaiActionPerformed
        Pegawai pegawai = new Pegawai();
        pegawai.setNip(inputNipPegawai.getText());
        pegawai.setNama(inputNamaPegawai.getText());
        pegawai.setTanggalLahir(inputTglLahirPegawai.getText());
        pegawai.setJabatan((Jabatan) dataJabatan.getSelectedItem());
        pegawai.setGolongan((Golongan) dataGolongan.getSelectedItem());
        pegawai.setNoTelp(inputNoTelphPegawai.getText());
        pegawai.setEmail(inputEmailPegawai.getText());
        pegawai.setAlamat(inputAlamatPegawai.getText());

        pegawai.save();
        tampilkanData();
    }//GEN-LAST:event_btnSimpanPegawaiActionPerformed

