    public void tampilkanData() {
        
        String[] kolom = {"ID", "NIP", "Nama", "No. Telp", "Email", "Tanggal Lahir", "Jabatan", "Golongan", "Alamat"};
        ArrayList<Pegawai> listPegawai = new Pegawai().getAll();
        Object[] rowData = new Object[9];

        tblPegawai.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (int i = 0; i < listPegawai.size(); i++) {
            rowData[0] = listPegawai.get(i).getIdPegawai();
            rowData[1] = listPegawai.get(i).getNip();
            rowData[2] = listPegawai.get(i).getNama();
            rowData[3] = listPegawai.get(i).getNoTelp();
            rowData[4] = listPegawai.get(i).getEmail();
            rowData[5] = listPegawai.get(i).getTanggalLahir();
            rowData[6] = listPegawai.get(i).getJabatan().getIdJabatan();
            rowData[7] = listPegawai.get(i).getGolongan().getIdGolongan();
            rowData[8] = listPegawai.get(i).getAlamat();
            
            ((DefaultTableModel) tblPegawai.getModel()).addRow(rowData);
        }
        
    }
    
    public void search(String keyword) {
        
        String[] kolom = {"ID Pegawai", "NIP", "Nama", "No. Telp", "Email", "Tgl. Lahir", "ID Jabatan", "ID Golongan", "Alamat"};
        ArrayList<Pegawai> listPegawai = new Pegawai().search(keyword);
        Object[] rowData = new Object[9];

        tblPegawai.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Pegawai p : listPegawai) {
            rowData[0] = p.getIdPegawai();
            rowData[1] = p.getNip();
            rowData[2] = p.getNama();
            rowData[3] = p.getNoTelp();
            rowData[4] = p.getEmail();
            rowData[5] = p.getTanggalLahir();
            rowData[6] = p.getJabatan().getIdJabatan();
            rowData[7] = p.getGolongan().getIdGolongan();
            rowData[8] = p.getAlamat();
            
            ((DefaultTableModel) tblPegawai.getModel()).addRow(rowData);
        }
        
    }
    
